package clasificacionTriplete;

public class VerificarMayor {

    public boolean numeroMayorIgual(int i, int x){

        return i >= x;
    }

    public boolean numeroMayor(int i, int x){

        return i > x;
    }

    public boolean numeroMenor(int i, int x){

        return i < x;
    }


}
